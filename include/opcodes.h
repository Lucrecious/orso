#ifndef OPCODES_H_
#define OPCODES_H_

#include "def.h"

typedef enum OrsoOPCode {
    ORSO_OP_NO_OP,

    ORSO_OP_POP,
    ORSO_OP_POPN,
    ORSO_OP_POP_SCOPE,

    ORSO_OP_I64_TO_F64,
    ORSO_OP_F64_TO_I64,

    ORSO_OP_ADD_I64, ORSO_OP_SUBTRACT_I64, ORSO_OP_MULTIPLY_I64, ORSO_OP_DIVIDE_I64,
    ORSO_OP_ADD_F64, ORSO_OP_SUBTRACT_F64, ORSO_OP_MULTIPLY_F64, ORSO_OP_DIVIDE_F64,

    ORSO_OP_NEGATE_I64, ORSO_OP_NEGATE_F64,

    ORSO_OP_EQUAL_I64, ORSO_OP_LESS_I64, ORSO_OP_GREATER_I64,
    ORSO_OP_EQUAL_F64, ORSO_OP_LESS_F64, ORSO_OP_GREATER_F64,

    ORSO_OP_EQUAL_SYMBOL,
    ORSO_OP_EQUAL_STRING, ORSO_OP_CONCAT_STRING,

    ORSO_OP_LOGICAL_NOT,

    ORSO_OP_PUSH_1,
    ORSO_OP_PUSH_0,

    // get constant (address: n-bits, size: byte < 8)
    ORSO_OP_CONSTANT_8BIT_ADDRESS,
    ORSO_OP_CONSTANT_16BIT_ADDRESS,
    ORSO_OP_CONSTANT_32BIT_ADDRESS,

    // set local (address: n-bits, size: byte)
    ORSO_OP_SET_LOCAL_8BIT_ADDRESS,
    ORSO_OP_SET_LOCAL_16BIT_ADDRESS,

    // get local (address: n-bits, size: byte < 8)
    ORSO_OP_GET_LOCAL_8BIT_ADDRESS,
    ORSO_OP_GET_LOCAL_16BIT_ADDRESS,
    
    // set global (address: n-bits, size: byte)
    ORSO_OP_SET_GLOBAL_8BIT_ADDRESS,
    ORSO_OP_SET_GLOBAL_16BIT_ADDRESS,
    ORSO_OP_SET_GLOBAL_32BIT_ADDRESS,

    // get global (address: n-bits, size: byte)
    ORSO_OP_GET_GLOBAL_8BIT_ADDRESS,
    ORSO_OP_GET_GLOBAL_16BIT_ADDRESS,
    ORSO_OP_GET_GLOBAL_32BIT_ADDRESS,

    // union stack manip
    ORSO_OP_PUT_IN_UNION,
    ORSO_OP_NARROW_UNION,

    // jumps
    ORSO_OP_JUMP_IF_FALSE,
    ORSO_OP_JUMP_IF_TRUE,
    ORSO_OP_JUMP_IF_UNION_FALSE,
    ORSO_OP_JUMP_IF_UNION_TRUE,
    ORSO_OP_JUMP,
    ORSO_OP_LOOP,

    // call
    ORSO_OP_CALL,
    ORSO_OP_RETURN,

    // builtin
    ORSO_OP_PRINT_EXPR,
    ORSO_OP_PRINT,
} OrsoOPCode;

#endif
